    private void actualizarPromedio() {
        int filas = jTableMaterias.getRowCount();
            if (filas == 0) {
                labelPromedio.setText("0.0");
                return;
                }

        double suma = 0;
        int cantidad = 0;

            for (int i = 0; i < filas; i++) {
                Object valor = jTableMaterias.getValueAt(i, 2); // columna 1 = calificación
            if (valor != null && !valor.toString().isEmpty()) {
              try {
                suma += Double.parseDouble(valor.toString());
                cantidad++;
            } catch (NumberFormatException e) {
                
            }
        }
    }

    if (cantidad > 0) {
        double promedio = suma / cantidad;
        labelPromedio.setText(String.format("%.2f", promedio)); // muestra con 2 decimales
    } else {
        labelPromedio.setText("0.0");
    }
    }


    
    
    private void cargarTabla() {
      modelo = new DefaultTableModel(
        new Object[]{"Materia", "Semestre", "Calificación",}, 0
          ) {
        //no quiero editar sobre la tabla, lo edita desde auqi
        @Override
        public boolean isCellEditable(int row, int column) {
            return false;
        }
    };

        jTableMaterias.setModel(modelo);
       modelo.setRowCount(0);

       double suma = 0;
       int contador = 0;

       for (Materias m : KardexDatos.listaMaterias) {
        double promedio = m.getPromedio();
        modelo.addRow(new Object[]{
            m.getNombre(),
            m.getSemestre(),
            m.getCalificaciones(),
            promedio
        });

        suma += promedio;
        contador++;
    }

    // calcula el promedio conforme agrego calificaciones directo al label 
    double promedioGeneral = (contador > 0) ? (suma / contador) : 0.0;
    labelPromedio.setText(String.format("%.2f", promedioGeneral));
    }

