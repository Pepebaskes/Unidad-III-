    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // boton para eliominar de la tabla
        int fila = jTableMaterias.getSelectedRow();
            if (fila == -1) {
                JOptionPane.showMessageDialog(this, "Selecciona una materia para eliminar.", "Advertencia", JOptionPane.WARNING_MESSAGE);
                return;
                }

        int confirm = JOptionPane.showConfirmDialog(this, "¿Deseas eliminar esta materia?", "Confirmar", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                KardexDatos.listaMaterias.remove(fila);
                cargarTabla();
                actualizarPromedio();
                JOptionPane.showMessageDialog(this, "Materia eliminada correctamente.");
                }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        //boton para modificar materias 
           int fila = jTableMaterias.getSelectedRow();
            if (fila == -1) {
                JOptionPane.showMessageDialog(this, "Selecciona una materia para modificar.", "Advertencia", JOptionPane.WARNING_MESSAGE);
                return;
                     }
                Materias materiaSeleccionada = KardexDatos.listaMaterias.get(fila);
                AgregarCalificaciones dialog = new AgregarCalificaciones(this, true, materiaSeleccionada);
                dialog.setVisible(true);
                cargarTabla();
                actualizarPromedio();
                JOptionPane.showMessageDialog(this, "Materia modificada correctamente.");
    }//GEN-LAST:event_btnModificarActionPerformed

